package dots.cg.java;

public class Switch {

	public static void main(String[] args) {
		int x= 20;
		switch (x) {
		
		case 45:
			System.out.println("the number is 45");
			
		case 34:
			System.out.println("the number is 34");
			
		case 3:
			System.out.println("the number is 3");
			
		case 20:
			System.out.println("the number is 20");
			System.out.println("found the num");
			
			default:
				  System.out.println("the number did not match any");
			
		
		}
		System.out.println("i am free from switch");

	}

}
